const NOISE_WORDS = new Set([
  'subscription', 'premium', 'plan', 'order', 'dinner', 'lunch', 'breakfast',
  'payment', 'purchase', 'fee', 'charge', 'bill', 'invoice', 'service',
  'monthly', 'annual', 'yearly', 'weekly', 'daily', 'india', 'indian',
  'unlimited', 'basic', 'standard', 'plus', 'pro', 'lite', 'max', 'ultra',
])

function normalizeName(name) {
  return name
    .toLowerCase()
    .trim()
    .replace(/[+＋]/g, ' plus ')
    .replace(/\s{2,}/g, ' ')
    .trim()
}

function stripNoise(normalized) {
  return normalized
    .split(/\s+/)
    .filter(w => !NOISE_WORDS.has(w) && w.length > 1)
    .join(' ')
    .trim()
}

/**
 * Maps a company/merchant name to a domain for logo fetching.
 * Works with exact names ("Netflix"), partial names ("Netflix Premium"),
 * and transaction titles ("Zomato Dinner").
 */
export function getDomain(name) {
  if (!name) return null

  const raw = normalizeName(name)
  const stripped = stripNoise(raw)

  // 1. Exact match on raw
  if (BRAND_DOMAINS[raw]) return BRAND_DOMAINS[raw]

  // 2. Exact match on noise-stripped version
  if (stripped && BRAND_DOMAINS[stripped]) return BRAND_DOMAINS[stripped]

  // 3. Raw name contains a known key (longest key wins to avoid false positives)
  let bestKey = '', bestDomain = ''
  for (const [key, domain] of Object.entries(BRAND_DOMAINS)) {
    if (raw.includes(key) && key.length > bestKey.length) {
      bestKey = key; bestDomain = domain
    }
  }
  if (bestDomain) return bestDomain

  // 4. First word lookup (handles "Zomato Dinner" → "zomato")
  const firstWord = raw.split(/\s+/)[0]
  if (firstWord && firstWord.length >= 4) {
    if (BRAND_DOMAINS[firstWord]) return BRAND_DOMAINS[firstWord]
    for (const [key, domain] of Object.entries(BRAND_DOMAINS)) {
      if (key === firstWord || (key.startsWith(firstWord) && firstWord.length >= 5)) return domain
    }
  }

  // 5. Try each meaningful word
  const words = raw.split(/\s+/).filter(w => w.length >= 4 && !NOISE_WORDS.has(w))
  for (const word of words) {
    if (BRAND_DOMAINS[word]) return BRAND_DOMAINS[word]
  }

  return null
}
